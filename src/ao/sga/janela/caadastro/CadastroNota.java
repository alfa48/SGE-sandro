    private void selectedElements()
    {
        alunoSeleted = prof.getTurma().get(comboboxTurmasCadastroNota.getSelectedIndex()).getAluno().get(comboboxAlunosCadastroNota.getSelectedIndex());
            if (alunoSeleted == null)
                return ;
            
            alunoSeleted.discs.forEach((d) -> {
                if (d.getName().equals(prof.getDisc().get(comboboxDisciplinsCadastroNota.getSelectedIndex()).getName()))
                {
                    disciplina = d;
                }
            });
    }
