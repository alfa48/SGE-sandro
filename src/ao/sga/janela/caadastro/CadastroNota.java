    public CadastroNota(ArrayList<Turma> turmas, JFrame janela, Prof prof) {
        initComponents();
        this.janela = janela;
        this.turmas = turmas;
        this.prof = prof;
        System.out.println(">>>>> PROFESSOR: "+prof.getName());
        comboboxTurmasCadastroNota.removeAllItems();
        comboboxDisciplinsCadastroNota.removeAllItems();
        comboboxAlunosCadastroNota.removeAllItems();
        
        //popular as turmas
        prof.getTurma().forEach((t) -> {
               comboboxTurmasCadastroNota.addItem(t.getName());
        });
        
        
        //alterei a turma populo as disciplinas e os alunos
        comboboxTurmasCadastroNota.addActionListener((ae) -> { 
    
             comboboxDisciplinsCadastroNota.removeAllItems();
            //Disciplinas que o prof leciona nesta turma
            prof.getTurma().get(comboboxTurmasCadastroNota.getSelectedIndex()).getDisc().forEach((d) -> {
                if (prof.getDisc().contains(d))
                     comboboxDisciplinsCadastroNota.addItem(d.getName());
            });
            comboboxAlunosCadastroNota.removeAllItems();
            //Alunos nesta Turma
            prof.getTurma().get(comboboxTurmasCadastroNota.getSelectedIndex()).getAluno().forEach((a) -> {
                comboboxAlunosCadastroNota.addItem(a.getName());                
            });
        });        
        
        //Seleciona Disciplina
        comboboxDisciplinsCadastroNota.addActionListener((ae) -> {
            popularListaAlunosMedias();
        });
        
        //Selecionei o alunos
        comboboxAlunosCadastroNota.addActionListener((ae) -> { 
            alunoSeleted = prof.getTurma()
                    .get(comboboxTurmasCadastroNota.getSelectedIndex())
                    .getAluno()
                    .get(comboboxAlunosCadastroNota.getSelectedIndex() < 0 ? 0 : comboboxAlunosCadastroNota.getSelectedIndex());
            System.out.println("ALUNO SELECIONADO EE: "+(alunoSeleted != null ? alunoSeleted.getName() : "Aluno desconhecido"));
        });
        
        modelList = new DefaultListModel<>();
        listaAlunosMediaCadastroNota.setModel(modelList);
        modelList.clear();

//        popularListaAlunoMedia(); 
    }  
    
    private void popularListaAlunosMedias()
    {
        System.err.println(">>> popularListaAlunosMedias()");
        //modelList.clear();
        //disciplina
        prof.getDisc()
                .get(comboboxDisciplinsCadastroNota.getSelectedIndex() < 0 ? 0 : comboboxDisciplinsCadastroNota.getSelectedIndex())
                .getAluno().forEach((a) -> {
                    if (a == null)
                        return ;
                    a.discs.forEach((d) -> {
                    if (d.getName().equals(prof.getDisc().get(comboboxDisciplinsCadastroNota.getSelectedIndex()).getName()))
                    {
                        modelList.addElement("Aluno: "+a.getName()+" | Disciplina: "+d.getName()+" | Me'dia: "+d.getMedia());
                    }
            }); 
        });
    }
    
    private void popularListaAlunoMedia()
    {
        //modelList.clear();
       selectedElements();//seleciona alunos e disciplinas
       
        if (alunoSeleted == null)
            return ;
        alunoSeleted.discs.forEach((d) -> {
            if (d.getName().equals(disciplina.getName()))
            {
                modelList.addElement("Aluno: "+alunoSeleted.getName()+" | Disciplina: "+disciplina.getName()+" | Me'dia: "+disciplina.getMedia());
            }
        });
    }
