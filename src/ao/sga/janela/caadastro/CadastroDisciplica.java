    private void btnGuardarCadastroDisciplinaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarCadastroDisciplinaActionPerformed

        if (!nomeCadastroDisciplina.getText().isEmpty() &&
            !ementaCadastroDisciplina.getText().isEmpty())
        {
            Turma turma = turmas.get(comboboxTurmasCadastroDisciplina.getSelectedIndex());
            turma.AddDisc(new Disc(nomeCadastroDisciplina.getText(), ementaCadastroDisciplina.getText()));
            nomeCadastroDisciplina.setText("");
            ementaCadastroDisciplina.setText("");
        }
    }//GEN-LAST:event_btnGuardarCadastroDisciplinaActionPerformed

