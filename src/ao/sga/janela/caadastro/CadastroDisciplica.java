    public CadastroDisciplica(ArrayList<Turma> turmas, JFrame janela) {
        initComponents();
        this.janela = janela;
        this.turmas = turmas;
        
        comboboxTurmasCadastroDisciplina.removeAllItems();
        turmas.forEach((t) -> {
            comboboxTurmasCadastroDisciplina.addItem(t.getName());
        });
        
        modelList = new DefaultListModel<>();
        listaDisciplina.setModel(modelList);
        popularListaAluno(0); 
        
        comboboxTurmasCadastroDisciplina.addActionListener((ae) -> {
            popularListaAluno(comboboxTurmasCadastroDisciplina.getSelectedIndex());
        });
    }

    private void popularListaAluno(int index)
    {
        modelList.clear();
        turmas.get(index).getDisc().forEach((a) -> {
            modelList.addElement("Nome: "+a.getName());
        });
    }
