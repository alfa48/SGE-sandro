    private void btnGuardarCadastroAlunoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarCadastroAlunoActionPerformed
        
        if (!nomeCadastroAluno.getText().isEmpty() &&
                !passwordCadastroAluno.getText().isEmpty() &&
                !userNameCadastroAluno.getText().isEmpty())
        {
            Turma turma = turmas.get(comboboxTurmasCadastroAlunos.getSelectedIndex());
            turma.AddAluno(new Aluno(nomeCadastroAluno.getText(), userNameCadastroAluno.getText(), passwordCadastroAluno.getText()));
            passwordCadastroAluno.setText("");
            nomeCadastroAluno.setText("");
            userNameCadastroAluno.setText("");
        }
    }//GEN-LAST:event_btnGuardarCadastroAlunoActionPerformed

