    public CadastroAluno(ArrayList<Turma> turmas, JFrame janela) {
        initComponents();
        this.janela = janela;
        this.turmas = turmas;
        
        comboboxTurmasCadastroAlunos.removeAllItems();
        turmas.forEach((t) -> {
            comboboxTurmasCadastroAlunos.addItem(t.getName());
        });
        
        modelList = new DefaultListModel<>();
        listaAlunos.setModel(modelList);
        popularListaAluno(0); 
        
        comboboxTurmasCadastroAlunos.addActionListener((ae) -> {
            popularListaAluno(comboboxTurmasCadastroAlunos.getSelectedIndex());
        });
    }
    
    private void popularListaAluno(int index)
    {
        modelList.clear();
        turmas.get(index).getAluno().forEach((a) -> {
            modelList.addElement("Nome: "+a.getName());
        });
    }
    
   

